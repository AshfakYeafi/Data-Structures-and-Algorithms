#include<iostream>
using namespace std;
int main() {
    int n;
    cout << "Enter N: " << endl;
    cin >> n;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j <= i; j++) {
            cout << "*";
        }
        for (int k = 2; (k < (2 * n - 2 * i)); k ++) {
            cout << " ";
        }
        for (int j = 0; j <= i; j++) {
            cout << "*";
        }
        cout << "\n";
    }

    for (int i = 0; i < n; i++) {
        for (int j = 0; j < (n - i); j++) {
            cout << "*";
        }
        for (int k = 0; (k < (2 * n) - (2 * n - 2 * i)); k ++) {
            cout << " ";
        }
        for (int j = 0; j < n - i; j++) {
            cout << "*";
        }
        cout << "\n";
    }
}